function coinChange(coins: number[], amount: number): number {
  let m = new Map();
  m.set(0, 0);
  for (let n = 1; n <= amount; n++) {
    let min = -1;
    for (let j = 0; j < coins.length; j++) {
      const px = m.get(n - coins[j]);
      if (px >= 0) {
        const pv = px + 1;
        min = min === -1 ? pv : Math.min(min, pv);
      }
    }
    m.set(n, min);
  }
  return m.get(amount) ?? -1;
}
